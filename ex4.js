//Vamos trabalhar com os dias da semana

var dia='Domingo';
switch(dia){
    case 'Segunda':
        console.log("hoje é o primeiro dia da semana");
        break;
    
 
    case 'Terça':
        console.log("hoje é o segundo dia da semana");
        break;
    case 'Quarta':
        console.log("Hoje e o terceiro dia da semana");
        break;
    case 'Quinta':
        console.log('Hoje é o quarto dia da semana');
        break;
    case 'Sexta':
        console.log('hoje é o quinto dia da semana');
        break;
    case 'Sábado':
        console.log('Hoje é o sexto dia da semana');
        break;
    case 'Domingo':
        console.log('Hoje é o sétimo e finalmente ultimo dia da semana');
        break;
    default:
        console.log('Informação inválida');
}